    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         String from_acc = txtfromacc.getText();
        
        try {
            //load Driver
            Class.forName("com.mysql.jdbc.Driver");
              //get Connection
            conn = DriverManager.
            getConnection("jdbc:mysql://localhost/bankmanagement1","root","");
            //sql to find customer from customer table based on his custid
            stmt1 = conn.prepareStatement("select * from account where acc_id=?");
            stmt1.setString(1, from_acc);
            //execute Query
            ResultSet rs = stmt1.executeQuery();
            //check results
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this, "No account found...");
            }
            //this sql returns all columns from customer table
            else{
               txtbalance.setText(rs.getString("balance"));
                               
            }
            
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Account.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Account.class.getName()).log(Level.SEVERE, null, ex);
        }
          
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

