    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
         String facc2 = txtfromacc.getText();
         String balance2 = txtbalance.getText();
         String toacc2 = txttoacc.getText();
         String amount2 = txtamount.getText();
         
         
        try {
            //load Driver
            Class.forName("com.mysql.jdbc.Driver");
               //get Connection
            conn = DriverManager.
            getConnection("jdbc:mysql://localhost/bankmanagement1","root","");
            
            conn.setAutoCommit(false);
            
            /***** reduce balance of from account by amount*/
            stmt1 = conn.prepareStatement("update account set balance=balance-? WHERE acc_id=?");
            stmt1.setString(1, amount2);
            stmt1.setString(2, facc2);
            stmt1.executeUpdate();
            /***** increase balance of to_account by amount*/
            stmt2 = conn.prepareStatement("update account set balance=balance+? WHERE acc_id=?");
            stmt2.setString(1, amount2);
            stmt2.setString(2, toacc2);
            stmt2.executeUpdate();
           
            ////FINALLY Insert transaction into transfer table
            stmt3 = conn.prepareStatement("insert into transfer(f_account,to_account,balance,amount,datetime) "+
                    "values(?,?,?,?,?)");
            stmt3.setString(1, facc2);
            stmt3.setString(2, toacc2);
            stmt3.setString(3, balance2);
            stmt3.setString(4, amount2);
            stmt3.setString(5, LocalDateTime.now().toString());
            stmt3.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Amount transfered !!! ");
            
            jButton4.doClick(); //clear form fields
            conn.commit();//commit to Databse NOW
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Transfer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            
            Logger.getLogger(Transfer.class.getName()).log(Level.SEVERE, null, ex);
             try {
                 conn.rollback();
             } catch (SQLException ex1) {
                 Logger.getLogger(Transfer.class.getName()).log(Level.SEVERE, null, ex1);
             }
            
        }
          
            
            
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


