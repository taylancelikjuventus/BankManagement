    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:

        irate = Double.parseDouble(txtIintRate.getText());
        noOfYears = Double.parseDouble(textNumOfYears.getText());
        amount = Double.parseDouble(textAmount.getText());

        try {

            //monthly interest rate
            payTotal = (amount * Math.pow(1 + irate / 1200, 12 * noOfYears));
            payMonth = payTotal / (12 * noOfYears);
            textTotalPay.setText(String.format("$ %.2f", payTotal));
            textMonthPay.setText(String.format("$ %.2f", payMonth));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnCalcActionPerformed

