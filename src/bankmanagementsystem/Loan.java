    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:

        //check acc id
        accid = textAccID.getText();
        Connection con;
        PreparedStatement ps;
        ResultSet res;

        if (!accid.isEmpty()) {

            con = DBConnection.connectToDB();

            try {
                ps = con.prepareStatement("select * from account WHERE acc_id=?");
                ps.setString(1, accid);
                res = ps.executeQuery();

                if (res.next()) {
                    txtIintRate.setEnabled(true);
                    textAmount.setEnabled(true);
                    textNumOfYears.setEnabled(true);
                }else{
                     JOptionPane.showMessageDialog(this, "No such an account found !");
                     btnClear.doClick();
                }

            } catch (SQLException ex) {
                Logger.getLogger(Loan.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {

            JOptionPane.showMessageDialog(this, "Please enter account id first !");

        }


    }//GEN-LAST:event_btnSearchActionPerformed

