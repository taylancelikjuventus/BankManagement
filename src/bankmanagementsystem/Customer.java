    /******Generate auto ID for each Customer***********/
    public void autoID() throws ClassNotFoundException, SQLException{
        //load Driver
        Class.forName("com.mysql.jdbc.Driver");
        //get Connection
        conn = DriverManager.
     getConnection("jdbc:mysql://localhost/bankmanagement1","root","");
        
        Statement s = conn.createStatement();
        //select maximum cust_id number from table
        ResultSet rs = s.executeQuery("SELECT Max(cust_id) from customer");
        rs.next();
        String maxid = rs.getString("Max(cust_id)");
        if(rs.getString("Max(cust_id)") == null){
            cust_no.setText("CS001");
        }else{
            //We generate customer id according to the last row's number in Customer table
            long id = Long.parseLong(rs.getString("Max(cust_id)")
             .substring(2, rs.getString("Max(cust_id)").length())
            );
            //increase id
            id++;
            
            cust_no.setText("CS" + String.format("%03d",id));
        }
    
    
    }
    
    /**************If user clicks Combobox for selecting branch**********/
    public void branch() throws ClassNotFoundException, SQLException{
        
           //load Driver
        Class.forName("com.mysql.jdbc.Driver");
        //get Connection
        conn = DriverManager.
        getConnection("jdbc:mysql://localhost/bankmanagement1","root","");
        //sql statement
        insert = conn.prepareStatement("select * from branch");
        //execute sql
        ResultSet rs = insert.executeQuery();
        //clear cb box
        txtbranch.removeAllItems();
        while(rs.next()){
            //branch tablosunun 2. kolonundaki datalarÄ± ekler combobox a.
            txtbranch.addItem(rs.getString(2));
            
        }
        
    }
    

