    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        //get fields from Customer.java GuiForm
        String customer_id = cust_no.getText();
        String firstname = txtfname.getText();
        String lastname = txtlname.getText();
        String street = txtstreet.getText();
        String city = txtcity.getText();
        String branch = (String)txtbranch.getSelectedItem();
        String phone = txtphone.getText();
        
         //insert fields into database
        try {
            //load Driver
            Class.forName("com.mysql.jdbc.Driver");
            //get Connection
            conn = DriverManager.
        getConnection("jdbc:mysql://localhost/bankmanagement1","root","");
            
            insert = conn.prepareStatement("insert into "+
                    "customer(cust_id,firstname,lastname,street,city,branch,phone) "+
                    "values(?,?,?,?,?,?,?)");
            
            insert.setString(1,customer_id);
            insert.setString(2,firstname);
            insert.setString(3,lastname);
            insert.setString(4,street);
            insert.setString(5,city);
            insert.setString(6,branch);
            insert.setString(7,phone);
            
            //execute sql
            insert.executeUpdate();
            
            //inform user with dialog window
            JOptionPane.showMessageDialog(this, "Rows inserted successfully ...");
            
            //Clering FORM for new Registration
            //clear fields from FORM to make it available for other customer registrations
            cust_no.setText("");
            txtfname.setText("");
            txtlname.setText("");
            txtcity.setText("");
            txtstreet.setText("");
            txtphone.setText("");
            txtbranch.setSelectedIndex(-1); //means no selection
            autoID(); //get available id from 
            txtfname.requestFocus(); //set focus on firstname field
            
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

